    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //remove book
        String b = jTextField11.getText().trim();
        Book b1 = Library.holdings.findBookFromTitle(b);
          Member ex = b1.getBorrower();
        if ( b1.toString() == null ? b != null : !b1.toString().equalsIgnoreCase(b))
        {
            JOptionPane.showMessageDialog(null, "No Such Book, Try again");
        }
        else {
            Book remBook = Library.holdings.findBookFromTitle(b);
            if (b1.isOnLoan())
            {
                  ex.returnBook(remBook); //returning from member
            }
            Library.holdings.removeBook(remBook);
            if (Library.availableBooks.findBookFromTitle(b) != null)
            {
                  Library.availableBooks.removeBook(remBook);
            }
          //remove from availablebooks, probably will error if book already removed
            availableBooks.setListData(Library.holdings.toArray());
              jTextField11.setText(null);
        }
 
    }//GEN-LAST:event_jButton6ActionPerformed

