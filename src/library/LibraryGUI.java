    private void loanBook() {
         String book = jTextField6.getText();
        String member = jTextField7.getText();
        
     availableBooks.setListData(Library.holdings.toArray());
       Member ex = Library.theMembers.getMemberFromName(member);
            if (ex.getBooksOnLoan().size()==3)
            {
                JOptionPane.showMessageDialog(null, "Maximum Book Limit reached!");    //this should be working
            }
                else
        if (Library.holdings.findBookFromTitle(book).isOnLoan()==true)
        {
            JOptionPane.showMessageDialog(null, "Book is on loan or not in Library.");
        }
        else {
             ex.borrowBook(Library.holdings.findBookFromTitle(book));
             Library.availableBooks.removeBook(Library.holdings.findBookFromTitle(book)); //add to available books 
             JOptionPane.showMessageDialog(null, "Book Loaned!");
        }
    
    }

    private void returnBook() {
     //return book
        String rBook = jTextField8.getText();
        Book b = Library.holdings.findBookFromTitle(rBook);
        String member =  jTextField9.getText().trim();
        
       Member ex = Library.theMembers.getMemberFromName(member);
       
       if (b.getBorrower() == null)
                {
                     JOptionPane.showMessageDialog(null,"No Book Owner");
                }
       else
        if (Library.holdings.findBookFromTitle(rBook).isOnLoan()==true)
        {
            ex.returnBook(b);
            Library.availableBooks.addBook(b); //add to available books
             JOptionPane.showMessageDialog(null, "Book Returned!");     
        }
        else 
        if (Library.holdings.findBookFromTitle(rBook).isOnLoan()==false)
                {
                JOptionPane.showMessageDialog(null, "Book not on loan");
                }
        else {
            JOptionPane.showMessageDialog(null, "Book is on loan or not in Library.");
        }
    }

    private void currentLoans() {
        String M = jTextField5.getText();
        Member Mem =Library.theMembers.getMemberFromName(M);
        String onLoan = Mem.getBooksOnLoan().toString();
       
        if (Mem.getBooksOnLoan().isEmpty())
        {
              JOptionPane.showMessageDialog(null, "This Member has no Books!");
        }
        else 
        {
              JOptionPane.showMessageDialog(null, "Currently on loan Books for this Member are: " + onLoan); 
        }
    }
