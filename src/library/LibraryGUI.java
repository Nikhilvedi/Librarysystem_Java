    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        
        String b1 = jTextField12.getText();
        Book b = Library.holdings.findBookFromTitle(b1);
        
        String Auth;
        String ISBN;
        int acc;
        String onloan;
        String loanstatus;
        String borrower; 
        //set values up 
        if (b.getBorrower() ==null) {    borrower = "";}  else {  borrower = b.getBorrower().toString();}
        if (b.getAuthor() == null) {   Auth = "";   }  else {   Auth = b.getAuthor();}
        if (b.getISBNNo() ==null)  {      ISBN = "";   }  else { ISBN = b.getISBNNo();}
        if (b.getAccessionNumber() == 0)  {  acc = 0;  } else    {   acc = b.getAccessionNumber();}
        if (b.isOnLoan() ==true)  {  onloan = "true";  } else    {   onloan = "false";}
        //get borrower
        if (onloan.equals("true"))
        {
            loanstatus = "On Loan to " + borrower;
        }
        else {
            loanstatus = "Not on Loan";
        }
        //create string to output
        if (b.getName().equalsIgnoreCase(b1))
        {
          
             JOptionPane.showMessageDialog(null, "Author: " + Auth + "\n ISBN: " + ISBN + "\n Accession Number: " + acc + "\n Loan Status: " + loanstatus );
        }
        else
        {
         JOptionPane.showMessageDialog(null,"Please enter valid Book");   
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

